<h1>Pengertian Framework</h1>
<br>
Framework (kerangka kerja) adalah istilah yang sering muncul dalam dunia developer. Istilah tersebut memiliki fungsi yang sangat besar bagi pengembangan kode program secara sistematis.<br>
Saat ini, seorang pengembang khususnya dalam bidang web development diharuskan untuk mempelajari dan menggunakan sebuah kerangka kerja dalam pembuatan perangkat lunak.<br>

Keuntungan lain adalah untuk mengembangkan perangkat lunak dengan penyusunan kode secara terstruktur dan konsisten. Kode yang baik tentu saja merupakan kode yang dapat dimengerti oleh mesin serta pengembang (developer).
